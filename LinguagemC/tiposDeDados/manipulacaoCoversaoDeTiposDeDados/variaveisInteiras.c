# include <stdio.h>

int main() {
    int a = 10;          // Variável inteira
    int b = 3;           // Outra variável inteira
    int soma = a + b;    // Soma de inteiros
    int diferenca = a - b; // Diferença de inteiros
    int produto = a * b; // Produto de inteiros
    int quociente = a / b; // Quociente de inteiros

    printf("Soma: %d\n", soma);
    printf("Diferença: %d\n", diferenca);
    printf("Produto: %d\n", produto);
    printf("Quociente: %d\n", quociente);

    return 0;
}