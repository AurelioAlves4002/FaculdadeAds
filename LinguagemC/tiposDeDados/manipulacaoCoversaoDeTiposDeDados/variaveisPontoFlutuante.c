#include <stdio.h>

int main() {
    float a = 10.5;      // Variável ponto flutuante
    float b = 3.2;       // Outra variável ponto flutuante
    float soma = a + b;  // Soma de ponto flutuante
    float diferenca = a - b; // Diferença de ponto flutuante
    float produto = a * b; // Produto de ponto flutuante
    float quociente = a / b; // Quociente de ponto flutuante

    printf("Soma: %.2f\n", soma);
    printf("Diferença: %.2f\n", diferenca);
    printf("Produto: %.2f\n", produto);
    printf("Quociente: %.2f\n", quociente);

    return 0;
}
// Exemplo de variáveis ponto flutuante